    private void btnDKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDKActionPerformed
        // TODO add your handling code here:
        int dk = JOptionPane.showConfirmDialog(this, "Bạn có muốn đăng kí","Thông báo", JOptionPane.YES_NO_OPTION);
        if(dk!= JOptionPane.YES_OPTION){
            return;
        }
        try{
            Connection con = new DBUtils().createConn();
            String SQL = "SELECT TenDN FROM KHACHHANG";
            Statement statement= con.createStatement();
            ResultSet rs=statement.executeQuery(SQL);
            while(rs.next()){
                if(txtUserName.getText().equals(rs.getString("TenDN")))
                {
                    JOptionPane.showMessageDialog(this, "Bị trùng tên đăng nhập, mời bạn nhập lại");
                }
            }
        } catch (SQLException ex) {
                ex.printStackTrace();
        }
        try{
            Connection con = new DBUtils().createConn();
            String sql = "insert into KHACHHANG (MAKH, TENKH, NGSINH, GIOITINH, EMAIL, SDT, TENDN, MK, TONGTIEN) VALUES (?, ?, ?, ?, ?, ?, ? ,?, 0)";
            PreparedStatement ps = con.prepareStatement(sql);
            ps.setString(1, LayMaKH());
            ps.setString(2, txtTenKH.getText());
            DateFormat df = new SimpleDateFormat("yyyy-MM-dd");
            ps.setString(3, df.format(dtcNgSinh.getDate()));
            if (cbbGT.getSelectedItem().toString().equals("Nữ") == true){
                ps.setBoolean(4, true);
            }
            else{
                ps.setBoolean(4, false);
            }
            ps.setString(5, txtEmail.getText());
            ps.setString(6, txtSDT.getText());
            ps.setString(7, txtUserName.getText());
            ps.setString(8, txtPassword.getText());
            int n = ps.executeUpdate();
            
            if(txtTenKH.getText().equals("")|dtcNgSinh.getDate().toString().equals("")|cbbGT.getSelectedItem().toString().equals("")|txtEmail.getText().equals("")|txtSDT.getText().equals("")|txtUserName.getText().equals("")|txtPassword.getText().equals(""))
            {
                JOptionPane.showMessageDialog(this, "Không để thông tin trống");
            }
            else if(n!=0){
                JOptionPane.showMessageDialog(this, "Đăng kí thành công");
                CustomerPage cp = new CustomerPage(txtUserName.getText());
            }
        
        }catch(Exception ex){
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnDKActionPerformed

