    public String LayMaKH(){
        String MaKH = "";
        Connection con = new DBUtils().createConn();
        String StrSQL = "SELECT MAKH FROM KHACHHANG order by MAKH DESC";
        try{
            Statement stat = con.createStatement(ResultSet.TYPE_SCROLL_INSENSITIVE,
                                  ResultSet.CONCUR_READ_ONLY);
            ResultSet rs = stat.executeQuery(StrSQL);
            if (rs.absolute(1)==false){
                MaKH="KH0001";
            }
            else {
                String MaKHHienTai = rs.getString("MaKH");
                MaKH = MaKHHienTai.substring(2,6);
                int MaKHmoi = Integer.parseInt(MaKH) +1;
                if(MaKHmoi<10)
                    MaKH = "KH000"+MaKHmoi;
                else
                    if(MaKHmoi<100)
                        MaKH = "KH00"+MaKHmoi;
                    else
                        if(MaKHmoi<1000)
                            MaKH = "KH0"+MaKHmoi;
                        else
                            MaKH = "KH"+MaKHmoi;
            }
            con.close();
        } catch (Exception e){
            e.printStackTrace();
            System.out.println("Lỗi");
        }
        return MaKH;
    }

